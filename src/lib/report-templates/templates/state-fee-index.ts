import { wrapReport, coverPage, sectionHeader, footnote } from "../index";

export interface StateFeeIndexReportInput {
  stateCode: string;
  stateName: string;
  generatedAt: string;
}

export function renderStateFeeIndexReport(
  input: StateFeeIndexReportInput,
): string {
  const { stateCode, stateName, generatedAt } = input;

  const body = [
    coverPage({
      title: `${stateName} Fee Index`,
      subtitle: `State-level fee analysis for ${stateCode}`,
      report_date: generatedAt,
    }),
    sectionHeader({ label: "Overview", title: `${stateName} Fee Landscape` }),
    `<p style="padding: 0 32px; color: #6b7280;">
      State fee index report template is under development.
      Full analysis for ${stateName} will be available in a future release.
    </p>`,
    footnote("Report generated by Bank Fee Index. Data subject to verification."),
  ].join("\n");

  return wrapReport(body, {
    title: `${stateName} Fee Index`,
    date: generatedAt,
  });
}
